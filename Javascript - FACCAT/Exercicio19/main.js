alert("Cálculo de número maior")
let numero1 = parseInt(prompt("Digíte o primeiro número:"))
let numero2 = parseInt(prompt("Digíte o segundo número:"))

if (numero1 > numero2) {
    alert(numero1 + " é maior que número " + numero2)
} else {
    alert(numero2 + " é maior que número " + numero1)
}